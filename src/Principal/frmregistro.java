    private void btnusernuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnusernuevoActionPerformed
        String nombre = txtnombre.getText();
        String apellido = txtapellido.getText();
        String email = txtemail.getText();
        String pass = txtpassword.getText();
        String tiporol = combotipo.getSelectedItem().toString();

        if (nombre.isEmpty() || apellido.isEmpty() || email.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe Completar Los Datos ");
        } else {
            if (tiporol.equalsIgnoreCase("Seleccionar")) {
                JOptionPane.showMessageDialog(null, "Debe De Seleccionar Un Tipo De Rol");
            } else {
                try {
                    String consulta = "INSERT INTO usuarios (nombre,apellido,email,clave,tipo_nivel)VALUES('" + nombre + "','" + apellido + "','" + email + "','" + pass + "','" + tiporol + "')";
                    PreparedStatement ps = cn.prepareStatement(consulta);
                    ps.executeUpdate();
                    limpar();
                    JOptionPane.showMessageDialog(null, "Datos Guardados Correctamente");
                    frg = new frmlogin();
                    frg.setVisible(true);
                    this.setVisible(false);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "No Se Pudo Guardar El Usuario" + e);
                }
            }

        }
    }//GEN-LAST:event_btnusernuevoActionPerformed

    void limpar() {
        txtnombre.setText("");
        txtapellido.setText("");
        txtemail.setText("");
        txtpassword.setText("");
    }

